    public void verificaVencedorJogo(int pontosSet1, int pontosSet2){
        int vencedor = placar.VerificaVencedor(pontosSet1, pontosSet2);
        if(vencedor == 1){
            JOptionPane.showMessageDialog(null, "A equipe 1 venceu o jogo");
        }else if(vencedor == 2){
              JOptionPane.showMessageDialog(null, "A equipe 2 venceu o jogo");
        }
    }
    public void verificaVencedor(int Vencedor) {
        if (Vencedor == 1) {
            JOptionPane.showMessageDialog(null, "O vencedor do set foi a equipe 1");
            placar.addSetAtual();
            placar.addSetEquipe1();
            tfSetsEquipe1.setText(Integer.toString(placar.getSetsEquipe1()));
            taMostrar.append(placar.getPontosEquipe1() + " - " + placar.getPontosEquipe2() + "\n");
            placar.ZerarPlacar();
            tfPontosEquipe2.setText(Integer.toString(placar.getPontosEquipe2()));
            tfPontosEquipe1.setText(Integer.toString(placar.getPontosEquipe1()));
            verificaVencedorJogo(placar.getSetsEquipe1(), placar.getSetsEquipe2());
            
        } else if (Vencedor == 2) {
            JOptionPane.showMessageDialog(null, "O vencedor do set foi a equipe 2");
            placar.addSetAtual();
            placar.addSetEquipe2();
            tfSetsEquipe2.setText(Integer.toString(placar.getSetsEquipe2()));
            taMostrar.append(placar.getPontosEquipe1() + " - " + placar.getPontosEquipe2() + "\n");
            placar.ZerarPlacar();
            tfPontosEquipe2.setText(Integer.toString(placar.getPontosEquipe2()));
            tfPontosEquipe1.setText(Integer.toString(placar.getPontosEquipe1()));
            verificaVencedorJogo(placar.getSetsEquipe1(), placar.getSetsEquipe2());
            
        }
    }
