            placar = new Placar(tfNomeEquipe1.getText(), tfNomeEquipe2.getText(), tfHorario.getText());

            tfPontosEquipe1.setText(Integer.toString(placar.getPontosEquipe1()));
            tfPontosEquipe2.setText(Integer.toString(placar.getPontosEquipe2()));
